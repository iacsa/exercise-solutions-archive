digit_chars =
  [["-**--","--*--","***--","***--","-*---","****-","-**--","****-","-**--","-**--"],
   ["*--*-","-**--","---*-","---*-","*--*-","*----","*----","---*-","*--*-","*--*-"],
   ["*--*-","--*--","-**--","-**--","****-","***--","***--","--*--","-**--","-***-"],
   ["*--*-","--*--","*----","---*-","---*-","---*-","*--*-","-*---","*--*-","---*-"],
   ["-**--","-***-","****-","***--","---*-","***--","-**--","-*---","-**--","-**--"],
   ["-----","-----","-----","-----","-----","-----","-----","-----","-----","-----"]]

IO.foreach(ARGV[0]){|line|
  output = Array.new(6, "")
  line.gsub(/[^0-9]/, "").chars.each do |c|
    (0..5).each do |i|
      output[i] += digit_chars[i][c.to_i]
    end
  end
  puts output
}
